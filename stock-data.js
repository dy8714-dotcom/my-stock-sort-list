/* ===== Generate preset tab data ===== */
function generatePresetTabs() {
  var presets = {};

  // Tab 18: JPX150 + Growth250
  presets[18] = [];
  presets[18].push({ type:"section", label:"JPX日経150" });
  JPX150.forEach(function(c) { presets[18].push({ code:c, name: STOCK_MASTER[c]||"" }); });
  presets[18].push({ type:"section", label:"グロース250" });
  GROWTH250.forEach(function(c) { presets[18].push({ code:c, name: STOCK_MASTER[c]||"" }); });

  // Tab 19: JPX400
  presets[19] = [];
  presets[19].push({ type:"section", label:"JPX日経400" });
  JPX400.forEach(function(c) { presets[19].push({ code:c, name: STOCK_MASTER[c]||"" }); });

  // Tab 20: Nikkei225 + TOPIX100
  presets[20] = [];
  presets[20].push({ type:"section", label:"日経225" });
  NIKKEI225.forEach(function(c) { presets[20].push({ code:c, name: STOCK_MASTER[c]||"" }); });
  presets[20].push({ type:"section", label:"TOPIX100" });
  var nk225Set = new Set(NIKKEI225);
  TOPIX100.forEach(function(c) {
    if (!nk225Set.has(c)) presets[20].push({ code:c, name: STOCK_MASTER[c]||"" });
  });

  // Tabs 21-29: by number range, sorted ascending, exclude A-codes
  for (var r = 1; r <= 9; r++) {
    var tabNum = 20 + r;
    var rangeStart = r * 1000;
    var rangeEnd = rangeStart + 999;
    presets[tabNum] = [];
    var codes = Object.keys(STOCK_MASTER).filter(function(c) {
      if (/[A-Za-z]/.test(c)) return false;
      var n = parseInt(c);
      return n >= rangeStart && n <= rangeEnd;
    }).sort(function(a,b) { return parseInt(a) - parseInt(b); });
    codes.forEach(function(c) { presets[tabNum].push({ code:c, name: STOCK_MASTER[c]||"" }); });
  }

  // Tab 30: all A-codes, sorted
  presets[30] = [];
  var aCodes = Object.keys(STOCK_MASTER).filter(function(c) { return /[A-Za-z]/.test(c); })
    .sort(function(a,b) { return a.localeCompare(b); });
  aCodes.forEach(function(c) { presets[30].push({ code:c, name: STOCK_MASTER[c]||"" }); });

  return presets;
}
