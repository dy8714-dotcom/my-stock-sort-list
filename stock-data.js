/** 銘柄コードから番台番号（1〜9）を取得。3桁・Aコードはnull */
function getBandai(code) {
  if (!code || code.length < 4) return null;
  if (/^[A-Z]/.test(code) || /[A-Z]$/.test(code)) return null; // Aコード
  const first = parseInt(code.charAt(0));
  if (first >= 1 && first <= 9) return first;
  return null;
}

/** 3桁コードまたはAコードか判定 */
function isSpecialCode(code) {
  if (!code) return false;
  if (code.length <= 3) return true;
  if (/[A-Z]/.test(code)) return true;
  return false;
}

/** マスタデータを番台別に分類 */
function getStocksByBandai(master) {
  const result = {};
  for (let i = 1; i <= 9; i++) result[i] = [];
  result['special'] = []; // 3桁+Aコード

  for (const code of Object.keys(master)) {
    if (isSpecialCode(code)) {
      result['special'].push({ code, name: master[code] });
    } else {
      const b = getBandai(code);
      if (b) result[b].push({ code, name: master[code] });
    }
  }

  // 各番台を昇順ソート
  for (const key of Object.keys(result)) {
    result[key].sort((a, b) => a.code.localeCompare(b.code));
  }
  return result;
}

/** インデックスから重複を除外したリストを作成 */
function mergeIndicesUnique(...indices) {
  const seen = new Set();
  const result = [];
  for (const list of indices) {
    for (const code of list) {
      if (!seen.has(code)) {
        seen.add(code);
        result.push(code);
      }
    }
  }
  return result;
}

/** コードから銘柄名を取得（マスタ優先、なければ不明） */
function getStockName(code, customMaster) {
  if (customMaster && customMaster[code]) return customMaster[code];
  if (STOCK_MASTER[code]) return STOCK_MASTER[code];
  return code + "（不明）";
}
